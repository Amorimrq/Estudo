#3. Maior de Idade* Faça um programa que peça a idade do usuário e exiba a mensagem "Maior de idade" 
# se ele tiver 18 anos ou mais, ou "Menor de idade" caso contrário. 
idade = int(input("Diga sua Idade: "))
if idade >= 18:
    print("Você é maior de idade")
else:
    print("Você é menor de idade")
